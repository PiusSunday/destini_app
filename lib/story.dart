class Story {
  String storyTitle;
  String choice1;
  String choice2;

  // CONSTRUCTOR FOR STORY CLASS
  Story({
    required this.storyTitle,
    required this.choice1,
    required this.choice2,
  }) {
    this.storyTitle = storyTitle;
    this.choice1 = choice1;
    this.choice2 = choice2;
  }
}
